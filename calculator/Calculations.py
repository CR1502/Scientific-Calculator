# Program that does calculations
import math

# 1. This function adds two numbers
def add(x, y):
    return x + y

# 2. This function subtracts two numbers
def subtract(x, y):
    return x - y

# 3. This function multiplies two numbers
def multiply(x, y):
    return x * y

# 4. This function divides two numbers
def divide(x, y):
    return x / y

# 5. This function gives us the floor division of two numbers
def floordiv(x, y):
    return x // y

# 6. This function gives us the modulus of two numbers
def modulus(x, y):
    return x % y
 
# 7. This function gives us the absloute value of a number
def absolute(x):
    return math.fabs(x)

# 8. This function gives us the factorial of a number
def fact(x):
    return math.factorial(x)

# 9. This function gives us the power of a numbers
def power(x, y):
    return math.pow(x, y)

# 10. This function gives us the GCD of two numbers
def gcd(x, y):
    return math.gcd(x, y)


# 11. This function is used to get the natural logarithm of a number
def nlog(x):
    return math.log(x)

# 12. This function is used to get the base-10 logarithm of a number
def log_10(x):
    return math.log10(x)

# 13. This function is used to get the base-10 logarithm of a number
